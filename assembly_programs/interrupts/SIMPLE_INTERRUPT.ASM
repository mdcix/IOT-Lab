ORG 0000H
	LJMP MAIN
ORG 0003H
	LJMP INTERRUPT_SERVICE
	
ORG 0030H
	MAIN:
		MOV P2,#0FFH
		MOV P1,#00H
		SETB IE.0
		SETB IE.7
		SETB TCON.0 //EDGE TRIGGERED
		;CLR TCON.0 //LEVEL TRIGGERED
		;DEFAULTLY LEVEL TRIGGERD
		
		LOOP:
		MOV A,P2
		MOV P1,A
		SJMP LOOP

INTERRUPT_SERVICE:
	CPL P0.0
	CPL P0.1
	CPL P0.2
	CPL P0.3
	RETI
END
