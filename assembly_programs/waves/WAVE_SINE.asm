ORG 0000
CLR A
CLR P0.7

ACALL STR

UP:
	MOV A,@R1
	ADD A,#127
	MOV P1,A
	INC R1
	CJNE R1,#3DH,UP
	
	MOV R1,#30H

DOWN:
	MOV A,@R1
	MOV B,A
	MOV A,#127
	SUBB A,B
	MOV P1,A
	INC R1
	CJNE R1,#3DH,DOWN
	
	MOV R1,#30H
	SJMP UP	

STR:
