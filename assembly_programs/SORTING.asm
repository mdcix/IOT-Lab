;SORTING
;NUMBER OF ELEMENTS  STORED IN 30H
;ELEMENTS ARE STORED FROM 40H
MOV R0,#30H
MOV A,@R0
DEC A
MOV R2,A
MOV R3,A
LOOP:
		MOV A,#40H
		MOV R0,A
		MOV R1,A
		INC R1
		MOV A,R2
		MOV R4,A
INNERLOOP:	
		MOV A,@R0
		MOV B,@R1
		CJNE A,B,CHK
CHK:
		JC SKIP ;ASCENDING 
		MOV @R0,B
		MOV @R1,A
SKIP:
		INC R0
		INC R1
		DJNZ R4,INNERLOOP
		DJNZ R3,LOOP
HLT: SJMP HLT





