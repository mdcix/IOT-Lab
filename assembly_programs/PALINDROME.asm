;LENGHT STORED AT 30H
;STRING STORED FROM 40H
;STORE RESULT AT 50H
MOV R0,#30H
MOV B,@R0
MOV R2,B

MOV R0,#40H
MOV A,#40H
ADD A,B
DEC A
MOV R1,A
LOOP:
		MOV A,@R0
		MOV B,@R1
		CJNE A,B,FALSE
		INC R0
		DEC R1
		DJNZ R2,LOOP
TRUE:
		MOV R0,#50H
		MOV @R0,#0FFH
		SJMP HLT
FALSE:
		MOV R0,#50H
		MOV @R0,#00H
HLT: SJMP HLT
		
		
